// 形参与实参的顺序/个数的关系.

// 实参的值,要赋给形参变量

function foo($x,$y,$z) {
    return 2*$x + 3*$y + $z;
}

echo foo(1,2,3),'<br />';

// 在调用的过程中,
// 实参,形参,从左到右,严格的逐一对应.[对号入座]
// 一一对应上,然后实参值,挨个儿赋给形参.



/****
如果实参少于形参,没对应上
 ****/
// echo foo(1,2); // warning警告,并尽量往下执行.



/****
如果实参多于形参,
 ****/

echo foo(1,2,3,4,5,6,7),'<br />'; // 还是11
// 因为实参对号给形参赋值时, 后面多出来的实参,将会被舍弃掉. 没有其他副作用.
