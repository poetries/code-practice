// 仔细判断,打出$arr来

list($arr[0],$arr[1],,$arr[2]) = array(3=>'赵',1=>'钱',5=>'孙',2=>'李',0=>'燕');
print_r($arr);

/**
分析:
先从左到右来对应着分配值

$arr[0] <====> array[0] 燕
$arr[1] <====> array[1] 钱
$arr[2] <====> array[3] 赵

分配完毕,开始赋值!!!!
从右到左赋值

$arr[2] = 赵
$arr[1] = 钱
$arr[0] = 燕


数组打印,并不是如你想的,按索引的大小来打印,
而是按创建时的顺序来打印.

因此,最终打印
array(
2=>'赵',
1=>'钱',
0=>'燕'
)
