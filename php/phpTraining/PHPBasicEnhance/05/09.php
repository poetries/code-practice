你想买1-31颗之间的珠子,
此商人 ,都能正好取出其中若干盒,正好能组成你要买的数量.

思路: 分成了几盒? 每盒又各是多少颗珠子呢?



分析: 计算机中,有一门很重要的课,叫信息论.
一种东西,他的状态,能用来标识信息.
