只给你2小时时间,
问:最少,需要几只老鼠

分析:
毒死需要2个小时,只给2个小时==>只能试1次.


分析: 2小时后,小白鼠,只有2种状态: 死/活

2只小白鼠,则有4种组合的状态.
N只小白鼠,则有2^N次组合的状态.


一共只有8只老鼠,最多需要8种状态,就能区分.
3只老鼠,组合8种状态.
因此,从理论上说, 3只老鼠足够了.


但3只老鼠如何试出来,自己用二进制的知识来组合一下.

