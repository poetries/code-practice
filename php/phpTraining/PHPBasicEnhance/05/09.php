想用来表示心情,最多能表示分种?
答:2种. 因为眼睛只有2种"态位"

如果有2只眼睛的话,每只眼睛都能睁能闭,

组合起来,就能表示4种心情.

比如:
左开右开
左开右闭
左闭右开
左闭右闭
