// 逻列一下比较运算符
/*
> 大于
< 小于

>= 大于等于
<= 小于等于

== 等于
!= 不等于


=== 全等于,(不仅值相等,连变量类型也相等)
!== 不全等于
*/


$a = 3;
$b = 2;

if($a > $b) {
    echo $a,'果然大于',$b;
} else {
    echo $a,'并不大于',$b;
}

echo '<br />';

if($a >= $b) { // >= 意味着,$a 比 $b大,或者==$b, 都行.
    echo $a,'果然大于等于',$b;
} else {
    echo $a,'并不大于等于',$b;
}

echo '<br />';


$a = 3;
$b = 3;
if($a >= $b) { // >= 意味着,$a 比 $b大,或者==$b, 都行.
    echo $a,'果然大于等于',$b;
} else {
    echo $a,'并不大于等于',$b;
}


