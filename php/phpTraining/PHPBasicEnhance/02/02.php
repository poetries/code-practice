}


/***
在不同的类型比较时,
PHP程序本身
会把变量转换成一致的类型,然后再去比较.



着重记以下几个转换
1: 布尔值 --> 字符串
2: 字符串 --> 数字的转换
3: 字符串 --> 布尔的转换
4: 数字 ---> 布尔的转换
5: 数字 ---> 字符串转换.
 ***/


/*
以5 == true比较为例
是把 整型5 ,转成布尔型.


其实有的语言中,根本就没有布尔型,都是拿 1, 0 充当 true/false用.
0, 0.0, 这两个值,转换成布尔是 false,
其他 都是true, 包括负数,也是true
*/
echo '<br />';
if(0.0 == true) {
    echo '0.0 == true';
} else {
    echo '0.0 != true';
}



echo '<br />';
if(-3 == true) {
    echo '-3 == true';
} else {
    echo '-3 != true';
}


/****
1: 布尔值 --> 字符串
true --> '1'
false ---> '';


2: 字符串 --> 数字的转换
从字符串左端开始找数字,直到第一个非数字结束,取出来的这一部分,即是转换后的值.

'3q' ===> 3
'123abc456' ===> 123
'fdsafdsaf789' === > 0


 ****/
echo '<hr />';
$num = 'fdsafdsaf789';
echo $num + 0; // 0



/**

3: 字符串 --> 布尔的转换
''字符串,'0',这种转换成 false,
其他一概是true

 **/
echo '<br />';
if('' == false) {
    echo '空字符串等于false';
} else {
    echo '空字符串不等于false';
}


echo '<br />';
if('0' == false) {
    echo '字符串0等于false';
} else {
    echo '字符串0不等于false';
}



/**
4:数字转布尔
只有整型的0,和浮点0.0, 转成false
其他,甚至负数,也转成ture
 **/




